package Model;

/**
 *
 * @author nuhupu
 */
public class Student {
    private String id,studentname,password;
    private int age;

    public Student(String id, String studentname, String password, int age) {
        this.id = id;
        this.studentname = studentname;
        this.password = password;
        this.age = age;
    }

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getStudentname() {
        return studentname;
    }

    public void setStudentname(String studentname) {
        this.studentname = studentname;
    }

    public String getPassword() {
        return password;
    }

    public void setPassword(String password) {
        this.password = password;
    }

    public int getAge() {
        return age;
    }

    public void setAge(int age) {
        this.age = age;
    }
}
